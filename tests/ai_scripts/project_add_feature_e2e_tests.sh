assert_contains() {
  local haystack="$1"
  local needle="$2"
  if [[ "$haystack" != *"$needle"* ]]; then
    echo "Assertion failed: expected output to contain: $needle" >&2
    echo "Actual output:" >&2
    echo "$haystack" >&2
    exit 1
  fi
}

assert_not_contains() {
  local haystack="$1"
  local needle="$2"
  if [[ "$haystack" == *"$needle"* ]]; then
    echo "Assertion failed: expected output to not contain: $needle" >&2
    echo "Actual output:" >&2
    echo "$haystack" >&2
    exit 1
  fi
}

assert_equal() {
  local expected="$1"
  local actual="$2"
  if [[ "$expected" != "$actual" ]]; then
    echo "Assertion failed: expected '$expected', got '$actual'" >&2
    exit 1
  fi
}

assert_nonzero_status() {
  local status="$1"
  if [[ "$status" -eq 0 ]]; then
    echo "Assertion failed: expected non-zero status" >&2
    exit 1
  fi
}

assert_zero_status() {
  local status="$1"
  if [[ "$status" -ne 0 ]]; then
    echo "Assertion failed: expected zero status, got $status" >&2
    exit 1
  fi
}

assert_file_exists() {
  local path="$1"
  if [[ ! -f "$path" ]]; then
    echo "Assertion failed: expected file to exist: $path" >&2
    exit 1
  fi
}

assert_file_not_exists() {
  local path="$1"
  if [[ -e "$path" ]]; then
    echo "Assertion failed: expected file to not exist: $path" >&2
    exit 1
  fi
}

setup_git_repo() {
  local repo_root="$1"

  mkdir -p "$repo_root"
  if [[ -z "$(find "$repo_root" -mindepth 1 -maxdepth 1 -print -quit)" ]]; then
    echo "seed" >"$repo_root/README.md"
  fi
  git -C "$repo_root" init >/dev/null 2>&1
  git -C "$repo_root" config user.name "Test User"
  git -C "$repo_root" config user.email "test@example.com"
  git -C "$repo_root" add -A
  git -C "$repo_root" commit -m "Initial commit" >/dev/null 2>&1
}

write_external_sources_yaml() {
  local target_path="$1"
  shift || true

  mkdir -p "$(dirname "$target_path")"

  if [[ "$#" -eq 0 ]]; then
    cat >"$target_path" <<'OUT'
sources: []
OUT
    return 0
  fi

  {
    echo "sources:"
    local entry=""
    local name=""
    local source_type=""
    for entry in "$@"; do
      name="${entry%%|*}"
      source_type="${entry#*|}"
      if [[ "$source_type" == "$entry" ]]; then
        source_type="generic"
      fi
      printf '  - name: %s\n' "$name"
      printf '    type: %s\n' "$source_type"
    done
  } >"$target_path"
}

