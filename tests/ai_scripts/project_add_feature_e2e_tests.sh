if [[ "${TEST_FAIL_SCRIPT:-}" == "$(basename "$0")" ]]; then
  echo "simulated failure from $(basename "$0")" >&2
  exit "${TEST_FAIL_SCRIPT_EXIT_CODE:-17}"
fi
